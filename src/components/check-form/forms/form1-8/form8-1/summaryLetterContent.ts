export function fillTokens(text: string, t: LetterTokens): string {
  return text
    .replace(/\{\{owner\}\}/g, t.owner ?? "-")
    .replace(/\{\{signTitle\}\}/g, t.signTitle ?? "-")
    .replace(/\{\{signature\}\}/g, t.signature ?? "-")
    .replace(/\{\{inspector\}\}/g, t.inspector ?? "-")
    .replace(/\{\{date\}\}/g, t.date ?? "-")
    .replace(/\{\{fixIssue\}\}/g, t.fixIssue ?? "-");
}
